using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace WSVirtualOffice.Models.querydata
{
    class Contact
    {
        private String contactname;
        private String phoneno;
        private String emailid;
        private int calls;
        private int visits;
        private String notes;
        private String lastupdated;
        private long customerno;



         
        public Contact(String contactname,
         String phoneno,
         String emailid,
         int calls,
         int visits,
         String notes,
         String lastupdated,long customerno)
        {
            this.contactname=contactname;
         this.phoneno=phoneno;
         this.emailid=emailid;
         this.calls=calls;
         this.visits=visits;
         this.notes=notes;
         this.lastupdated = lastupdated;
         this.customerno = customerno;

        }


        


        public String Contactname
        {
            get { return this.contactname; }
            set { this.contactname = value; }
        }

        public String Phoneno
        {
            get { return this.phoneno; }
            set { this.phoneno = value; }
        }

        public String Emailid
        {
            get { return this.emailid; }
            set { this.emailid = value; }
        }

        public int Calls
        {
            get { return this.calls; }
            set { this.calls = value; }
        }

        public int Visits
        {
            get { return this.visits; }
            set { this.visits = value; }
        }

        public String Notes
        {
            get { return this.notes; }
            set { this.notes = value; }
        }

        public String Lastupdated
        {
            get { return this.lastupdated; }
            set { this.lastupdated = value; }
        }

        public long Customerno
        {
            get { return this.customerno; }
            set { this.customerno = value; }
        }


    }
}
