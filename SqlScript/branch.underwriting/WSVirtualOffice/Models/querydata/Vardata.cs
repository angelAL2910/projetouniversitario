using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace WSVirtualOffice.Models.querydata
{
    class Vardata
    {
        private String fromyear;
        private String toyear;
        private decimal amount;
        private long num;


        public Vardata(String fromyear, String toyear, decimal amount, long num)
        {
            this.fromyear = fromyear;
            this.toyear = toyear;
            this.amount = amount;
            this.num = num;


        }


        public String Fromyear
        {
            get { return this.fromyear; }
            set { this.fromyear = value; }
        }

        public String Toyear
        {
            get { return this.toyear; }
            set { this.toyear = value; }
        }

        public decimal Amount
        {
            get { return this.amount; }
            set { this.amount = value; }
        }

        public long Num
        {
            get { return this.num; }
            set { this.num = value; }
        }



    }
}
