using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace WSVirtualOffice.Models.querydata
{
    class GroupTypeGrid
    {
        public GroupTypeGrid(long code, String name, DateTime date, String status)
        {
            this.code = code;
            this.name = name;
            this.date = date.ToShortDateString();
            this.status = status;
        }


        private long code;

        public long Code
        {
            get { return code; }
            set { code = value; }
        }
        private String name;

        public String Name
        {
            get { return name; }
            set { name = value; }
        }
        private String date;

        public String Date
        {
            get { return date; }
            set { date = value; }
        }
        private String status;

        public String Status
        {
            get { return status; }
            set { status = value; }
        }

    }
}