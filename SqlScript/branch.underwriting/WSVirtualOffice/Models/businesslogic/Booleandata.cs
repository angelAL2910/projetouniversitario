using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace WSVirtualOffice.Models.businesslogic
{
    class Booleandata
    {
        public static char getBooleanvalue(String str1)
        {
            //str1 = Lookuplangdata.getEnglishvalue(Lookuptables.generaldet, str1);
            if (str1.Equals("Yes"))
            {
                return 'Y';
            }
            else if (str1.Equals("No"))
            {
                return 'N';
            }
            else
            {
                return ' ';
            }

        }

        public static String getBooleanstring(char str1)
        {
            if (str1=='Y')
            {
                return "Yes";
            }
            else if (str1 == 'N')
            {
                return "No";
            }
            else
            {
                return " ";
            }

        }
    }
}
